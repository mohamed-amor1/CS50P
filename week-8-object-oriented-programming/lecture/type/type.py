# Print the type of various objects using the type() built-in function.
# These 'types' are all in reality 'classes'


# Print the type of an integer literal
print(type(50))

# Print the type of a string literal
print(type("hello, world"))

# Print the type of an empty list literal
print(type([]))

# Print the type of an empty list created using the list() constructor
print(type(list()))

# Print the type of an empty dictionary literal
print(type({}))

# Print the type of an empty dictionary created using the dict() constructor
print(type(dict()))
